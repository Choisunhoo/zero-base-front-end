/**
 * 산술 연산자
 * + - / * %
 */

console.log(1 + 1); //  2
console.log(2 - 1); //  1
console.log(10 / 5); //  2
console.log(10 % 2); //  0
console.log(2 * 2); //  4


let x = 5;
--x; 
console.log(x); // 4
x++;
console.log(x); // 5
x = x + 1;
console.log(x); // 6
x += 1;
console.log(x); // 7

console.log(3 + 10 * 2); // 23
console.log(3 + (10 * 2)); // 23
console.log((3 + 10) * 2); // 26